  base.m + row_number() OVER () AS num,
  r.code, r.name, r.category, r.price, r.unit, r.supplier, r.date, r.reference, r.note,
  'import-quotes-2026-05' AS created_by
FROM rows r CROSS JOIN base
WHERE NOT EXISTS (
  SELECT 1 FROM proc_history h
  WHERE h.code = r.code
    AND h.supplier = r.supplier
    AND h.date = r.date
    AND COALESCE(h.reference,'') = COALESCE(r.reference,'')
);

