--  الهجرة 042 — سدّ فجوة الوظائف الفارغة (حوكمة أدوار) — بعد 040
--  المشروع: mwbjoysuybgbrvfrprex (البوابة، نظام 3). idempotent — مدمجة في standalone.
--
--  الفجوة (من تدقيق تغطية الصلاحيات مقابل الوظائف):
--   • وظيفة `gm` (المدير العام) كانت صلاحياتها `{}` فارغة، بينما مرحلة «اعتماد المدير
--     العام» في سلسلة أمر الشراء (الشرائح >250,000) تتطلّب مفتاح `can_manage_users`
--     (انظر portal_build_po_chain). فمدير عام غير-أدمن بوظيفة gm كان بلا أي صلاحية
--     ولا يستطيع اعتماد مرحلته → تتعطّل الطلبات الكبيرة عنده.
--   • وظيفة `qc` (مراقب جودة) كانت `{}` فارغة، فلا تشارك في الاستلام/فحص المرتجعات.
--
--  القرار (خبير تقني/إداري — أقل امتياز + فصل مهام):
--   • المدير العام **مشارك في سير العمل خاضع لفصل المهام، لا أدمن كامل يتجاوز الضوابط**.
--     يُمنَح `can_manage_users` (هويّته لمرحلة PO + إدارة مستخدمين محدودة بلا تصعيد،
--     محكومة بحُرّاس منع الترقية الذاتية) — دون جعله admin (الذي يُعفيه من كل SoD).
--   • مراقب الجودة يُمنَح `can_verify_stock` (تسجيل الاستلام والمرتجعات/التالف).
--
--  آمن: الدمج بـ`||` يضيف المفتاح فقط دون مسّ أي تخصيص مالك قائم؛ والشرط `NOT (… ? …)`
--  يجعلها idempotent ولا تُعيد الكتابة إن كانت الصلاحية ممنوحة مسبقاً.
-- ════════════════════════════════════════════════════════════════════════════

-- (أ) المدير العام: هوية مرحلة اعتماد أمر الشراء العليا + إدارة مستخدمين محدودة.
UPDATE portal_jobs
   SET permissions = coalesce(permissions,'{}'::jsonb) || '{"can_manage_users":true}'::jsonb,
       description  = 'اعتماد أعلى شرائح أمر الشراء + إدارة المستخدمين والإعدادات (بلا تصعيد صلاحيات). ليس أدمن نظام.'
 WHERE key = 'gm'
   AND NOT coalesce((permissions->>'can_manage_users')::boolean, false);

-- (ب) مراقب الجودة: المشاركة في الاستلام وتسجيل المرتجعات/التالف.
UPDATE portal_jobs
   SET permissions = coalesce(permissions,'{}'::jsonb) || '{"can_verify_stock":true}'::jsonb,
       description  = 'فحص مطابقة المواصفات + تسجيل الاستلام والمرتجعات/التالف (إشعار مدين).'
 WHERE key = 'qc'
   AND NOT coalesce((permissions->>'can_verify_stock')::boolean, false);
